	IIT offers an innovative and comprehensive approach to providing students with a real-world project-based experience – the integration of interprofessional perspectives in a student team environment. Developed at IIT in 1995, the IPRO Program consists of student teams from the junior through graduate levels, representing the breadth of the university’s disciplines and professional programs. 

The multidisciplinary IPRO team project course experience is offered via three platforms that provide students a variety of choices that tap their passion and disciplinary knowledge. IPRO projects also offer sponsors, community partners and other collaborators customized approaches to accommodate their interests. All three IPRO platforms are designed to achieve the same learning objectives and serve our undergraduate students in fulfilling their two-IPRO six-credit-hour general education requirement. At the traditional end of semester IPRO Day event, IPRO teams from all platforms exhibit their work for the semester, with variations in how teams from different platforms are evaluated. 
